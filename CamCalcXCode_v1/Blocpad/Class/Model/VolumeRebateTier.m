#import "VolumeRebateTier.h"

@implementation VolumeRebateTier

@synthesize min, max, rebate;

- (id)initWithValues:(double)_rebate min:(int)_min max:(int)_max {
    if ((self = [super init])) {
        rebate = _rebate;
        min = _min;
        max = _max;
    }
    return self;
}

@end
